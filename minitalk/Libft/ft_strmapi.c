#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	size_t	j;
	char	*str;

	if (!s || !f)
		return (0);
	i = 0;
	j = ft_strlen(s);
	str = (char *)malloc(j + 1);
	if (!str)
		return (0);
	while (i < j)
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
